function myFunction1() {

    var x = document.getElementById("ans1");
    var y = document.getElementById("ans4");
    var z = document.getElementById("ans2");
    var a = document.getElementById("ans3");
    var b = document.getElementById("ans5");
    var c = document.getElementById("ans6");

    if (x.style.display == "none") {
        x.style.display = "block";
        y.style.display = "none";
        z.style.display = "none";
        a.style.display = "none";
        b.style.display = "none";
        c.style.display = "none";
    } else {
        x.style.display = "none";
    }
}
